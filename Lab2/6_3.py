# Python - Change Dictionary Items


# 1st question: Consider the following code:
x = {'type' : 'fruit', 'name' : 'banana'}
# What is a correct syntax for changing the type from fruit to berry?
#Answer: x['type'] = 'berry'

# 2nd question: Change the "year" value from 1964 to 2020. 
car =	{
  "brand": "Ford",
  "model": "Mustang",
  "year": 1964
}
#Answer: car["year"] = 2020

# 3rd question: Consider the following code:
x = {'type' : 'fruit', 'name' : 'banana'}
# What is a correct syntax for changing the name from banana to apple?
#Answer: x.update({'name': 'apple'})
