# Python Booleans

# 1st question: What will be the result of the following syntax:
print(5 > 3)
#Answer: True

# 2nd question: The statement below would print a Boolean value, which one?
print(10 > 9)
#Answer: True

# 3rd question: The statement below would print a Boolean value, which one?
print(10 == 9)
#Answer: False

# 4th question: The statement below would print a Boolean value, which one?
print(10 < 9)
#Answer: False

# 5th question: The statement below would print a Boolean value, which one?
print(bool("abc"))
#Answer: True

# 6th question: The statement below would print a Boolean value, which one?
print(bool(0))
#Answer: False