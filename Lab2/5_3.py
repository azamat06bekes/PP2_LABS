# Python - Add Set Items


# 1st question: What is a correct syntax for adding items to a set?
#Answer: add()

# 2nd question: Use the add method to add "orange" to the fruits set.
fruits = {"apple", "banana", "cherry"}
#Answer: fruits.add("orange")

# 3rd question: Use the correct method to add multiple items (more_fruits) to the fruits set.
fruits = {"apple", "banana", "cherry"}
more_fruits = ["orange", "mango", "grapes"]
#Answer: fruits.update(more_fruits)
