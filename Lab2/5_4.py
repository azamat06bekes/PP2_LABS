# Python - Remove Set Items


# 1st question: What is a correct syntax for removing an item from a set?
#Answer: remove()

# 2nd question: Use the remove method to remove "banana" from the fruits set.
fruits = {"apple", "banana", "cherry"}
#Answer: fruits.remove("banana")

# 3rd question: Use the discard method to remove "banana" from the fruits set.
fruits = {"apple", "banana", "cherry"}
#Answer: fruits.discard("banana")

# 4th question: Select the correct function to remove all items from a set:
fruits = {'apple', 'banana', 'cherry'}
#Answer: fruits.clear()
