# Python - Access List Items


# 1st question: What will be the result of the following syntax:
mylist = ['apple', 'banana', 'cherry']
print(mylist[-1])
#Answer: cherry

# 2nd question: Print the second item in the fruits list.
fruits = ["apple", "banana", "cherry"]
print(fruits[1])
#Answer: fruits[1]

# 3rd question: What will be the result of the following syntax:
mylist = ['apple', 'banana', 'cherry', 'orange', 'kiwi']
print(mylist[1:4])
#Answer: ['banana', 'cherry', 'orange']

# 4th question: Use a range of indexes to print the third, fourth, and fifth item in the list.
fruits = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
print(fruits[2:5])
#Answer: fruits[2:5]
