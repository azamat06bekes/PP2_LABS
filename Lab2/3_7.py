# Python - List Comprehension


# 1st question: Consider the following code:
fruits = ['apple', 'banana', 'cherry']
newlist = [x for x in fruits if x == 'banana']
# What will be the value of newlist?
#Answer: ['banana']

# 2nd question: Fill in the missing parts to complete the list comprehension:
fruits = ["apple", "banana", "cherry"]
#Answer: newlist = [x for x in fruits]

# 3rd question: Consider the following code:
fruits = ['apple', 'banana', 'cherry']
newlist = ['apple' for x in fruits]
# What will be the value of newlist?
#Answer: ['apple', 'apple', 'apple']
