# Python - Remove List Items


# 1st question: What is a List method for removing list items?
#Answer: pop()

# 2nd question: Use the remove method to remove "banana" from the fruits list.
fruits = ["apple", "banana", "cherry"]
#Answer: fruits.remove("banana")

# 3rd question: What will be the result of the following syntax:
mylist = ['apple', 'banana', 'cherry']
mylist.pop(1)
print(mylist)
#Answer:  ['apple', 'cherry']

# 4th question: Select the correct function to remove all items from a list:
fruits = ['apple', 'banana', 'cherry']
#Answer: fruits.clear()
