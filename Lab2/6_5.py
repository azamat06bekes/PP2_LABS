# Python - Remove Dictionary Items


# 1st question: What is a dictionary method for removing an item from a dictionary?
#Answer: pop()

# 2nd question: Use the pop method to remove "model" from the car dictionary.
car =	{
  "brand": "Ford",
  "model": "Mustang",
  "year": 1964
}
#Answer: car.pop("model")

# 3rd question: Use the clear method to empty the car dictionary.
car =	{
  "brand": "Ford",
  "model": "Mustang",
  "year": 1964
}
#Answer: car.clear()

