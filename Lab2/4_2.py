# Python - Access Tuple Items


# 1st question: You can access tuple items by referring to the index number, but what is the index number of the first item?
#Answer: 0

# 2nd question: Use the correct syntax to print the first item in the fruits tuple.
fruits = ("apple", "banana", "cherry")
#Answer: print(fruits[0])

# 3rd question: Use negative indexing to print the last item in the tuple.
fruits = ("apple", "banana", "cherry")
#Answer: print(fruits[-1])

# 4th question: Use a range of indexes to print the third, fourth, and fifth item in the tuple.
fruits = ("apple", "banana", "cherry", "orange", "kiwi", "melon", "mango")
#Answer: print(fruits[2:5])
