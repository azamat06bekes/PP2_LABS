# Python - Add List Items


# 1st question: What will be the result of the following syntax:
mylist = ['apple', 'banana', 'cherry']
mylist.insert(0, 'orange')
print(mylist[1])
#Answer: apple

# 2nd question: Use the append method to add "orange" to the fruits list.
fruits = ["apple", "banana", "cherry"]
#Answer: fruits.append("orange")

# 3rd question: Use the insert method to add "lemon" as the second item in the fruits list.
fruits = ["apple", "banana", "cherry"]
#Answer: fruits.insert(1, "lemon")

# 4th question: Select the missing parts to add the elements of tropical to fruits:
fruits = ['apple', 'banana', 'cherry']
tropical = ['mango', 'pineapple', 'papaya']
#Answer: fruits.extend(tropical)
