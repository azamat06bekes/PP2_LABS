# Python Data Types


# 1st question: If x = 5, what is a correct syntax for printing the data type of the variable x?
#Answer: print(type(x))

# 2nd question: The following code example would print the data type of x, what data type would that be?
x = 5
print(type(x))
#Answer: int

# 3rd question: The following code example would print the data type of x, what data type would that be?
x = "Hello World"
print(type(x))
#Answer: str

# 4th question: The following code example would print the data type of x, what data type would that be?
x = 20.5
print(type(x))
#Answer: float

# 5th question: The following code example would print the data type of x, what data type would that be?
x = ["apple", "banana", "cherry"]
print(type(x))
#Answer: list

# 6th question: The following code example would print the data type of x, what data type would that be?
x = ("apple", "banana", "cherry")
print(type(x))
#Answer: tuple

# 7th question: The following code example would print the data type of x, what data type would that be?
x = {"name" : "John", "age" : 36}
print(type(x))
#Answer: dict

# 8th question: The following code example would print the data type of x, what data type would that be?
x = True
print(type(x))
#Answer: bool
